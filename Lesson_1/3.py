# 3.	По введенным пользователем координатам двух точек вывести
# уравнение прямой вида y = kx + b, проходящей через эти точки.

ax, ay = input("Введите координаты точки A(x;y) ").split(";")
bx, by = input("Введите координаты точки B(x;y) ").split(";")

ax = float(ax)
ay = float(ay)
bx = float(bx)
by = float(by)


k = (ay - by) / (ax - bx)
b = by - k * bx
print(f"\nУравнение прямой, проходящей через эти точки:\ny = {k:.2f} * x + {b:.2f}")
